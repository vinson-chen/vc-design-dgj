/**
 * 扫描 assets/store_logo，生成可直接用于 img.src 的 URL 映射。
 *
 * 说明：
 * - 之前使用 file loader 导出的字符串通常是 `./xxx-xxxx.jpg` 这种相对路径，
 *   在 Vite SPA 中会被当作路由路径，导致图片请求失败。
 * - 现在改为使用 `new URL(relative, import.meta.url).href`，让结果在运行时变成可用的绝对 URL。
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const pkgRoot = path.resolve(__dirname, '..');
const assetsDir = path.join(pkgRoot, 'assets', 'store_logo');
const outFile = path.join(pkgRoot, 'src', 'generated', 'storeLogoUrls.ts');

if (!fs.existsSync(assetsDir)) {
  console.error('Missing', assetsDir);
  process.exit(1);
}

const files = fs
  .readdirSync(assetsDir)
  .filter((f) => /\.(jpg|jpeg|png|gif)$/i.test(f))
  .sort();

const mapEntries = [];

files.forEach((f, i) => {
  const key = f.replace(/\.(jpg|jpeg|png|gif)$/i, '').toLowerCase();
  // 说明：tsup 将 src 内代码打进 dist/index.js 后，import.meta.url 的基准变成 dist 文件所在目录（packages/vc-biz/dist）。
  // assets 在 packages/vc-biz/assets/store_logo，因此运行时相对路径应为 '../assets/store_logo/...'
  mapEntries.push(`  '${key}': new URL('../assets/store_logo/${f}', import.meta.url).href,`);
});

const content = `/* eslint-disable -- auto-generated by scripts/generate-store-logo-map.mjs */
export const storeLogoUrlByKey: Record<string, string> = {
${mapEntries.join('\n')}
};
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, content, 'utf8');
console.log('Wrote', path.relative(pkgRoot, outFile), 'entries:', files.length);
