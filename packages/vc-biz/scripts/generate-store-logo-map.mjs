/**
 * 扫描 assets/store_logo，生成 store 图标 URL 映射。
 *
 * 使用静态 import + tsup file loader，将 jpg 复制到 dist 并替换为可解析的 URL。
 * 避免 `new URL('../assets/...', import.meta.url)` 在以下场景失效：
 * - Demo 通过 Vite alias 走源码时，基准是 src/generated/，../assets 会错指到 src/assets；
 * - Vite 预打包依赖时，import.meta.url 落在 .vite/deps，相对路径找不到资源；
 * - npm 包仅发布 dist 时，若未把图片打进 dist，运行时 URL 无效。
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const pkgRoot = path.resolve(__dirname, '..');
const assetsDir = path.join(pkgRoot, 'assets', 'store_logo');
const outFile = path.join(pkgRoot, 'src', 'generated', 'storeLogoUrls.ts');

if (!fs.existsSync(assetsDir)) {
  console.error('Missing', assetsDir);
  process.exit(1);
}

const files = fs
  .readdirSync(assetsDir)
  .filter((f) => /\.(jpg|jpeg|png|gif)$/i.test(f))
  .sort();

const imports = [];
const mapLines = [];

files.forEach((f, i) => {
  const key = f.replace(/\.(jpg|jpeg|png|gif)$/i, '').toLowerCase();
  const varName = `storeLogoAsset_${i}`;
  // 自 src/generated 指向资源目录
  imports.push(`import ${varName} from '../../assets/store_logo/${f}';`);
  mapLines.push(`  '${key}': ${varName},`);
});

const content = `/* eslint-disable -- auto-generated by scripts/generate-store-logo-map.mjs */
${imports.join('\n')}

export const storeLogoUrlByKey: Record<string, string> = {
${mapLines.join('\n')}
};
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, content, 'utf8');
console.log('Wrote', path.relative(pkgRoot, outFile), 'entries:', files.length);
